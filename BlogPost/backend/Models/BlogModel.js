import mongoose from "mongoose";

const blogSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true,
    },
    content: {
      type: String,
      required: true,
    },
    image: {
      type: String,
    },
    author: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "auth", // your Auth model name
      required: true,
    },
  },
  { timestamps: true }
);

export const Blog = mongoose.model("blog", blogSchema);
